            <h3>Contratiempos</h3>
            <p>
            En marzo de 2013 una cápsula Dragon tuvo problemas con los propulsores debido a unas válvulas bloqueadas. Los ingenieros de SpaceX fueron capaces de arreglarlo remotamente pero debido a este problema la nave llegó un día más tarde de lo esperado a la Estación Espacial Internacional.
            <p>
                En septiembre de 2016, un Falcon 9 explotó en la plataforma de lanzamiento durante el proceso de repostaje para la última prueba antes del lanzamiento. La carga, el Amos-6 de Spacecom valorado en 200 millones de dólares, fue destruida. Elon Musk describió el evento como "el fallo más complejo y difícil" en la historia de SpaceX.
