            <h2>Historia</h2>
            <p>Space Exploration Technologies Corporation es una empresa estadounidense de transporte aeroespacial fundada en 2002 por Elon Musk, quien es también cofundador de PayPal, y fundador de Tesla Motors, SolarCity, Hyperloop, The Boring Company y OpenAI.</p>
        
            <h2>Cosas Logradas</h2>
            <p>Ha desarrollado los cohetes Falcon 1, Falcon 9 y Falcon Heavy los cuales han sido construidos con la meta de ser vehículos de lanzamiento espacial reutilizables. SpaceX también ha desarrollado la nave espacial Dragon que fue puesta en órbita por los vehículos de lanzamiento Falcon 9, también se encuentran desarrollando el BFR o Star Ship una nave la cual planea usar para enviar colonias de humanos en futuras misiones a Marte. SpaceX diseña, prueba y fabrica la mayor parte de los componentes por cuenta propia, incluyendo los motores de cohete Merlín, Kestrel y Draco.</p>

        <div id="valor">
            <h2>Empresa Similar</h2>
            Blue Origin es una empresa estadounidense de transporte aeroespacial fundada en el 2000 por Jeff Bezos, también fundador de Amazon. Entre sus objetivos se encuentran los vuelos sub orbitales y orbitales, tanto para misiones oficiales de Estados Unidos, como para vuelos privados, Su principal trabajo son los cohetes reutilizables por medio de descensos controlados de los mismos por propulsión luego de los lanzamientos.
