            <h3>Algo de historia</h3>
            <p>En 2001, Elon Musk conceptualizó Mars Oasis, un proyecto para enviar un invernadero experimental en miniatura a Marte y cultivar plantas en un intento para recuperar el interés público en la exploración espacial y aumentar el presupuesto de la NASA. Musk intentó comprar cohetes baratos de Rusia pero volvió con las manos vacías al ser incapaz de encontrarlos a un precio aceptable.
